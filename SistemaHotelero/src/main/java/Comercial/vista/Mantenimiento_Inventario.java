    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ARTICULO");
        modelo.addColumn("NOMBRE DE ARTICULO");
        modelo.addColumn("CARACTERISTICAS");
        modelo.addColumn("REFERENCIA FABRICANTE");
        modelo.addColumn("PRECIO DE COMPRA");
        modelo.addColumn("MARGEN");
        modelo.addColumn("DECUENTO DE COMPRA");
        modelo.addColumn("DESCUENTO DE VENTA");
        modelo.addColumn("CANTIDAD DE ARTICULO");
        modelo.addColumn("CADUCIDAD DE ARTICULO");
        InventarioDAO inventarioDAO = new InventarioDAO();
        List<Inventario> inventarios = inventarioDAO.select();
        tablaInventario.setModel(modelo);
        String[] dato = new String[10];
        for (int i = 0; i < inventarios.size(); i++) {
            dato[0] = Integer.toString(inventarios.get(i).getId_Inventario());
            dato[1] = inventarios.get(i).getNombre_Articulo();
            dato[2] = inventarios.get(i).getCaracteristica_Articulo();
            dato[3] = inventarios.get(i).getReferecia_fabricante();
            dato[4] = inventarios.get(i).getPrecio_Compra();
            dato[5] = inventarios.get(i).getMargen();
            dato[6] = inventarios.get(i).getDescuento_Compra();
            dato[7] = inventarios.get(i).getDescuento_Venta();
            dato[8] = inventarios.get(i).getCantidad_Articulo();
            dato[9] = inventarios.get(i).getCaducidad_Articulo();

//System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

